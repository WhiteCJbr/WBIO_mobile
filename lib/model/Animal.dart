class Animal {
  int? id;
  String nomePopular;
  String habitat;
  String peso;
  String alimentacao;
  String filo;
  String classe;
  String ordem;
  String familia;
  String genero;
  String especie;
  String descricao;
  String imagem;

  Animal({
    this.id,
    required this.nomePopular,
    required this.habitat,
    required this.peso,
    required this.alimentacao,
    required this.filo,
    required this.classe,
    required this.ordem,
    required this.familia,
    required this.genero,
    required this.especie,
    required this.descricao,
    required this.imagem
  });

}